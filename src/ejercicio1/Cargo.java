class Cargo {
    private String nombre;
    private double valorHora;

    public Cargo(String nombre, double valorHora) {
        this.nombre = nombre;
        this.valorHora = valorHora;
    }

    public String getNombre() {
        return nombre;
    }

    public double getValorHora() {
        return valorHora;
    }
}
